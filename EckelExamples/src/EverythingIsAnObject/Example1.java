package EverythingIsAnObject;

public class Example1 {
    /**
     * Not initialized fields
     */
    int intVal;
    char charVal;

    /**
     * Printing values of fields
     * 
     * @param args
     *            Main Method
     */
    public void printValues() {
	System.out.println("intVal initialized as= " + intVal
		+ " charVal initialized as= " + charVal);
    }

}
