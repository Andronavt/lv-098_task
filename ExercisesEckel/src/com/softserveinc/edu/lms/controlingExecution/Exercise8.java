public class Exercise8 {

    /**
     * @param args
     */
    public static void main(String[] args) {
	for (int i = 0; i <= 3; i++) {
	    int indexCase = i;
	    switch (indexCase) {
	    case 1:
		System.out.println("case 1");
		break;
	    case 2:
		System.out.println("case 2");
		break;
	    case 3:
		System.out.println("case 3");
		break;
	    default:
		System.out.println("default case");
		break;
	    }
	}
    }

}
