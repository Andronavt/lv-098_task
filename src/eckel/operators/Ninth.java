package eckel.operators;

/*
 * Display the largest and smallest number for both float and double exponential notation.
 */

public class Ninth {
    public static void main(String[] args) {
	System.out.println("float max_ex: " + Float.MAX_EXPONENT);
	System.out.println("float min_ex: " + Float.MIN_EXPONENT);
	System.out.println("double max_ex: " + Double.MAX_EXPONENT);
	System.out.println("double min_ex: " + Double.MIN_EXPONENT);
    }

}
